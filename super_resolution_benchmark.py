# ---------------- image I/O (RGB uint8 everywhere) ----------------
def imread_rgb(path):
    bgr = cv2.imread(path, cv2.IMREAD_COLOR)
    if bgr is None:
        raise FileNotFoundError(path)
    return cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)

def imwrite_rgb(path, rgb):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    cv2.imwrite(path, cv2.cvtColor(rgb.astype(np.uint8), cv2.COLOR_RGB2BGR))

def modcrop(img, scale):
    h, w = img.shape[:2]
    return img[:h - (h % scale), :w - (w % scale)]

# ---------------- BT.601 YCbCr (matches the SRCNN training code) ----------------
def rgb2ycbcr(img):
    img = img.astype(np.float64)
    y  = 16.  + (64.738 * img[..., 0] + 129.057 * img[..., 1] + 25.064 * img[..., 2]) / 256.
    cb = 128. + (-37.945 * img[..., 0] - 74.494 * img[..., 1] + 112.439 * img[..., 2]) / 256.
    cr = 128. + (112.439 * img[..., 0] - 94.154 * img[..., 1] - 18.285 * img[..., 2]) / 256.
    return np.stack([y, cb, cr], axis=-1)

def ycbcr2rgb(img):
    img = img.astype(np.float64)
    r = 298.082 * img[..., 0] / 256. + 408.583 * img[..., 2] / 256. - 222.921
    g = 298.082 * img[..., 0] / 256. - 100.291 * img[..., 1] / 256. - 208.120 * img[..., 2] / 256. + 135.576
    b = 298.082 * img[..., 0] / 256. + 516.412 * img[..., 1] / 256. - 276.836
    return np.stack([r, g, b], axis=-1)

def to_y(rgb):
    return rgb2ycbcr(rgb)[..., 0]

# ---------------- metrics (Y channel, border shaved) ----------------
def evaluate(sr_rgb, hr_rgb, shave=SHAVE):
    if sr_rgb.shape[:2] != hr_rgb.shape[:2]:
        sr_rgb = cv2.resize(sr_rgb, (hr_rgb.shape[1], hr_rgb.shape[0]), interpolation=cv2.INTER_AREA)
    y_sr, y_hr = to_y(sr_rgb), to_y(hr_rgb)
    if shave > 0:
        y_sr = y_sr[shave:-shave, shave:-shave]
        y_hr = y_hr[shave:-shave, shave:-shave]
    psnr = sk_psnr(y_hr, y_sr, data_range=255.0)
    ssim = sk_ssim(y_hr, y_sr, data_range=255.0,
                   gaussian_weights=True, sigma=1.5, use_sample_covariance=False)
    return float(psnr), float(ssim)

# ---------------- generic per-method runner ----------------
def run_method(name, reconstruct):
    """reconstruct(base_filename, lr_rgb, hr_rgb) -> super-resolved RGB at HR size.

    Iterates every dataset in DATASETS, saves each SR output to
    results/<name>/<dataset>/<image>.png, and writes per-dataset PSNR/SSIM
    into RESULTS[dataset][name] = (avg_psnr, avg_ssim, per_image_dict).
    """
    method_root = os.path.join(OUT_DIR, name)
    for dataset, stems in DATASETS.items():
        if not stems:
            continue
        out_dir = os.path.join(method_root, dataset)
        os.makedirs(out_dir, exist_ok=True)
        psnrs, ssims, per_img = [], [], {}
        print('  --- %s / %s ---' % (name, dataset))
        for stem in stems:
            hr_path = os.path.join(HR_DIR, stem + '.png')
            lr_path = os.path.join(LR_DIR, stem + '.png')
            if not (os.path.exists(hr_path) and os.path.exists(lr_path)):
                continue
            hr = imread_rgb(hr_path)
            lr = imread_rgb(lr_path)
            sr = reconstruct(stem + '.png', lr, hr)
            imwrite_rgb(os.path.join(out_dir, stem + '.png'), sr)
            p, s = evaluate(sr, hr)
            psnrs.append(p); ssims.append(s); per_img[stem] = (p, s)
            print('    %-14s PSNR %6.2f dB   SSIM %.4f' % (stem + '.png', p, s))
        if psnrs:
            avg = (float(np.mean(psnrs)), float(np.mean(ssims)), per_img)
            RESULTS.setdefault(dataset, {})[name] = avg
            print('    %-14s PSNR %6.2f dB   SSIM %.4f' % ('AVERAGE', avg[0], avg[1]))
    return RESULTS

