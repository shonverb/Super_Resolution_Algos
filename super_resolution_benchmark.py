def process_image(input_path, methods=('bicubic', 'srcnn', 'esrgan'), scale=SCALE):
    """Run the chosen methods on a file and save outputs to CUSTOM_OUT.
    Returns {method: out_path}."""
    lr = imread_rgb(input_path)
    stem, _ = os.path.splitext(os.path.basename(input_path))
    print('Input : %s  shape=%s' % (input_path, lr.shape))
    paths = {}
    for m in methods:
        sr = upscale(lr, method=m, scale=scale)
        out_path = os.path.join(CUSTOM_OUT, '%s_%s_x%d.png' % (stem, m, scale))
        imwrite_rgb(out_path, sr)
        paths[m] = out_path
        print('  %-7s -> %s  shape=%s' % (m, out_path, sr.shape))
    return paths

def show_comparison(input_path, paths):
    """Show the LR input alongside each method's output."""
    import matplotlib.pyplot as plt
    lr = imread_rgb(input_path)
    panels = [('Input (LR)', lr)] + [(m.upper(), imread_rgb(p)) for m, p in paths.items()]
    fig, axes = plt.subplots(1, len(panels), figsize=(4 * len(panels), 4))
    if len(panels) == 1:
        axes = [axes]
    for ax, (t, img) in zip(axes, panels):
        ax.imshow(img); ax.set_title('%s  %s' % (t, img.shape[:2])); ax.axis('off')
    plt.tight_layout(); plt.show()

