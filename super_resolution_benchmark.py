def _remap_basicsr_state(state):
    """BasicSR layer names -> xinntao/ESRGAN RRDBNet layer names."""
    def remap(k):
        k = k.replace('conv_body', 'trunk_conv')
        k = k.replace('body.', 'RRDB_trunk.')
        k = k.replace('.rdb', '.RDB')
        k = k.replace('conv_up1', 'upconv1').replace('conv_up2', 'upconv2')
        k = k.replace('conv_hr', 'HRconv')
        return k
    if any(k.startswith('body.') or k.startswith('conv_body') for k in state):
        state = {remap(k): v for k, v in state.items()}
    return state

