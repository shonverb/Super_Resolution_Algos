def looks_like_pytorch_checkpoint(path):
    # Real .pth files are either zip-format (>=PyTorch 1.6, starts with 'PK') or legacy
    # pickle (starts with 0x80). HTML error pages start with '<' - that is the failure
    # mode of the original Dropbox URL.
    if not os.path.exists(path) or os.path.getsize(path) < 50_000:
        return False
    with open(path, 'rb') as f:
        head = f.read(4)
    return head[:2] == b'PK' or head[:1] == b'\x80'

