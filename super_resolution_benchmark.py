@torch.no_grad()
def esrgan_infer(lr_rgb):
    """Run the x4 ESRGAN model on an RGB uint8 image. Returns RGB uint8 at 4x size."""
    img = lr_rgb.astype(np.float32) / 255.0
    tensor = torch.from_numpy(img.transpose(2, 0, 1))[None].to(DEVICE)
    out = esrgan_model(tensor).clamp(0.0, 1.0).cpu().numpy()[0]
    return (out.transpose(1, 2, 0) * 255.0).round().clip(0, 255).astype(np.uint8)

