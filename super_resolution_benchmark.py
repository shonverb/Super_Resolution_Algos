import torch.nn as nn

class SRCNN(nn.Module):
    # 9-5-5 architecture from yjn870/SRCNN-pytorch (single Y-channel plane).
    def __init__(self, num_channels=1):
        super().__init__()
        self.conv1 = nn.Conv2d(num_channels, 64, kernel_size=9, padding=9 // 2)
        self.conv2 = nn.Conv2d(64, 32, kernel_size=5, padding=5 // 2)
        self.conv3 = nn.Conv2d(32, num_channels, kernel_size=5, padding=5 // 2)
        self.relu  = nn.ReLU(inplace=True)
    def forward(self, x):
        x = self.relu(self.conv1(x))
        x = self.relu(self.conv2(x))
        return self.conv3(x)

def safe_load(path, map_location='cpu'):
    # PyTorch >= 2.6 defaults to weights_only=True; fall back if a checkpoint needs it.
    try:
        return torch.load(path, map_location=map_location, weights_only=True)
    except Exception:
        return torch.load(path, map_location=map_location, weights_only=False)

def try_load_weights(model, path):
    state = safe_load(path, DEVICE)
    if isinstance(state, dict) and 'state_dict' in state:
        state = state['state_dict']
    # Some forks prefix keys (e.g. 'module.conv1.weight'); strip a single leading prefix.
    if isinstance(state, dict) and state and all('.' in k for k in state):
        first = next(iter(state))
        prefix = first.split('.')[0]
        if prefix not in ('conv1', 'conv2', 'conv3'):
            state = {k.split('.', 1)[1]: v for k, v in state.items() if '.' in k}
    model.load_state_dict(state, strict=True)

