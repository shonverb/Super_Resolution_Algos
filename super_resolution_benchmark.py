# Generic single-image upscaler that wraps the three methods. The SRCNN model
# was instantiated in the SRCNN cell; the ESRGAN model in the ESRGAN cell.
@torch.no_grad()
def upscale(lr_rgb, method='bicubic', scale=SCALE):
    """Upscale an RGB uint8 image by `scale`.
    method in {'bicubic', 'srcnn', 'esrgan'}. Returns an RGB uint8 array."""
    h, w = lr_rgb.shape[:2]
    tgt_h, tgt_w = h * scale, w * scale
    m = method.lower()

    if m == 'bicubic':
        return cv2.resize(lr_rgb, (tgt_w, tgt_h), interpolation=cv2.INTER_CUBIC)

    if m == 'srcnn':
        bicubic = cv2.resize(lr_rgb, (tgt_w, tgt_h), interpolation=cv2.INTER_CUBIC)
        ycbcr = rgb2ycbcr(bicubic)
        y = (ycbcr[..., 0] / 255.0).astype(np.float32)
        inp = torch.from_numpy(y)[None, None].to(DEVICE)
        out = srcnn(inp).clamp(0.0, 1.0).cpu().numpy()[0, 0] * 255.0
        merged = np.stack([out, ycbcr[..., 1], ycbcr[..., 2]], axis=-1)
        return np.clip(ycbcr2rgb(merged), 0, 255).astype(np.uint8)

    if m == 'esrgan':
        sr_x4 = esrgan_infer(lr_rgb)
        return cv2.resize(sr_x4, (tgt_w, tgt_h), interpolation=cv2.INTER_AREA)

    raise ValueError('Unknown method: %s. Choose bicubic / srcnn / esrgan.' % method)

