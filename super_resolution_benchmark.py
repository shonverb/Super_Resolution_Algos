def download_valid(urls, dst, min_bytes=10_000_000):
    import urllib.request
    for url in urls:
        try:
            print('Downloading', url[:90], '...')
            req = urllib.request.Request(url, headers={'User-Agent': 'Mozilla/5.0'})
            with urllib.request.urlopen(req, timeout=120) as r, open(dst, 'wb') as f:
                while True:
                    chunk = r.read(1 << 20)
                    if not chunk: break
                    f.write(chunk)
        except Exception as e:
            print('  failed:', type(e).__name__, e)
            continue
        if os.path.getsize(dst) < min_bytes:
            print('  too small (%d bytes) - probably an error page' % os.path.getsize(dst))
            continue
        with open(dst, 'rb') as f:
            head = f.read(2)
        if head[:1] == b'<':
            print('  got HTML, not a checkpoint')
            continue
        if head == b'PK' or head[:1] == b'\x80':
            return True
        print('  unrecognised magic bytes:', head)
    return False

